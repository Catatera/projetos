function abrirResposta0() {
    let resposta0 = document.querySelector('p#resposta0') //primeiro p
    if (resposta0.style.display == 'none') {
        resposta0.style.display = 'inline'
    }
    else {
        resposta0.style.display = 'none'
    }
}
